package com.github.jinahya.openrtb.domain;

public enum PredefinedApiFramework implements ApiFrameworkCode {

    // -----------------------------------------------------------------------------------------------------------------
    VPAID_1_0(1),

    VPAID_2_0(2),

    MRAID_1_0(3),

    ORMMA(4),

    MRAID_2_0(5),

    MRAIN_3_0(6),

    OMID_1_0(7);

    // -----------------------------------------------------------------------------------------------------------------
    PredefinedApiFramework(final int code) {
        this.code = code;
    }

    // -----------------------------------------------------------------------------------------------------------------
    @Override
    public int getCode() {
        return code;
    }

    // -----------------------------------------------------------------------------------------------------------------
    private final int code;
}
